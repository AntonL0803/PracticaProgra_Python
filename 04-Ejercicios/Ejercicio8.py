#Primera situacion
i = 5
while i >= 0:
    print(i*i)
    i-=1
#Segunda situacion
i = 0
while i <= 5:
    print(i*i)
    i+=1
#Tercera situacion
for i in range (5, -1, -1):
    print(i*i)
#Cuarta situacion
i = 5+1
while i>0:
    i-=1
    print(i*i)
#Quinta situacion
i = 0
while i <= 5:
    print((5 - i)*(5-i))
    i+=1
#Sexta situacion
for i in range (0, 5, +1):
    print(i*i)
    